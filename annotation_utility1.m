%Annotation utility – facilitates semi-automated scoring and structured saving of annotations, aligning with the rubric-based framework.
% ========================================================================
% Script: annotate_bread.m
% Purpose: Semi-automated annotation tool for bread crumb dataset
% Author: Ronnie O. Serfa Juan et al.
% ------------------------------------------------------------------------
% This script loads bread crumb images one by one and prompts the user to
% assign porosity, texture, and color gradient scores based on a 1–5 rubric.
% The annotations are then stored in the metadata CSV for standardization.
% ========================================================================

clear; clc;   % Clear workspace variables and command window for a fresh start

% ------------------------------------------------------------------------
% Load the metadata table generated by generate_metadata.m
% This CSV contains the list of all image filenames with empty annotation fields
% ------------------------------------------------------------------------
metadata = readtable('bread_metadata.csv');

% ------------------------------------------------------------------------
% Define the folder where the bread crumb images are stored
% Change this path if your images are saved in a different location
% ------------------------------------------------------------------------
imageDir = 'E:\BREAD_PROJECT\images\';

% ------------------------------------------------------------------------
% Loop through each row (i.e., each image entry) in the metadata table
% "height(metadata)" gives the number of rows in the table
% ------------------------------------------------------------------------
for i = 1:height(metadata)
    
    % --------------------------------------------------------------------
    % Read and display the current image based on its filename
    % fullfile() ensures the correct path is built (folder + filename)
    % --------------------------------------------------------------------
    img = imread(fullfile(imageDir, metadata.Image_ID{i}));
    imshow(img);   % Display the image in a figure window
    title(sprintf('Annotating image: %s', metadata.Image_ID{i})); % Show filename as title
    
    % --------------------------------------------------------------------
    % Prompt the user to input scores using a standardized 1–5 rubric
    % These inputs overwrite the placeholder NaN values in the metadata
    % --------------------------------------------------------------------
    metadata.Porosity_Score(i) = input('Enter porosity score (1–5): ');
    metadata.Texture_Score(i)  = input('Enter texture score (1–5): ');
    metadata.Color_Gradient(i) = input('Enter color gradient score (1–5): ');
end

% ------------------------------------------------------------------------
% After all images are annotated, save the updated metadata into a new CSV
% This file preserves the assigned quality scores alongside image IDs
% ------------------------------------------------------------------------
writetable(metadata, 'bread_metadata_annotated.csv');

% ------------------------------------------------------------------------
% Display confirmation message to indicate successful annotation
% ------------------------------------------------------------------------
disp('Annotation complete: bread_metadata_annotated.csv');

